from pandas import Series

date = ['2021-05-31','2021-06-01','2021-06-02','2021-06-03','2021-06-04']
xrp_close = [512,508,512,507,500]
s = Series(xrp_close, index=date)

s['2021-06-05'] = 490						# 인덱스 2021-06-05 의 값을 추가
print(s.drop('2021-06-01'))					# 인덱스 2021-06-01 를 제외하고 출력
print('*'*80)
print(s)									# 시리즈를 전체 출력하여 비교(추가한 것 확인, 제외 출력 비교)

