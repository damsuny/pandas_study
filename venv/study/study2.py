from pandas import Series

date = ['2021-05-31','2021-06-01','2021-06-02','2021-06-03','2021-06-04']
xrp_close = [512,508,512,507,500]
s = Series(xrp_close, index=date)			# xrp_close의 인덱스(라이브러리는 키-밸류, 시리즈는 인덱스-밸류)를 date로 지정
print(s)
print('*'*80)
print(s.index)								# 인덱스만 출력
print('*'*80)
print(s.values)								# 밸류만 출력
print('*'*80)
print(s[['2021-05-31','2021-06-03']])		# 일부분만 출력
print('*'*80)
print(s['2021-05-31':'2021-06-03'])			# 일부분만 출력
print('*'*80)
print(s[1:4])								# 일부분만 출력

